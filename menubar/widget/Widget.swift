                            }
                        }
                        Spacer()
                        RingView(value: a.ctxPct, label: "context", tint: ctxColor(a.ctxPct))
                            .frame(width: 64, height: 64)
                    }
                    Divider().opacity(0.4)
                    BarRow(label: "5h session", value: a.session5hPct, tint: ctxColor(a.session5hPct))
