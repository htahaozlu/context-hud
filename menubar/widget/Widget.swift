// MARK: - Data

struct HudSnapshot {
    struct AgentInfo {
        let name: String
        let project: String
        let ctxPct: Double?
        let model: String?
        let session5hPct: Double?
        let week7dPct: Double?
        let activeTokens: UInt64
        let lastTurn: Date?
    }
    let active: AgentInfo?
    let agents: [AgentInfo]
    let generatedAt: Date

    static let empty = HudSnapshot(active: nil, agents: [], generatedAt: Date())

    static let appGroupID = "DQJT5BCZCM.com.htahaozlu.contextbar"

    static func load() -> HudSnapshot {
        let fm = FileManager.default
        var candidates: [URL] = []
        if let groupURL = fm.containerURL(forSecurityApplicationGroupIdentifier: appGroupID) {
            candidates.append(groupURL.appendingPathComponent("hud.json"))
        }
        candidates.append(URL(fileURLWithPath: "\(NSHomeDirectory())/.context-bar/hud.json"))

        var loaded: Data?
        for url in candidates {
            if let data = try? Data(contentsOf: url) {
                loaded = data
                break
            }
        }
        guard
            let data = loaded,
            let root = try? JSONSerialization.jsonObject(with: data) as? [String: Any]
        else { return .empty }

        let claude = parse(root["claude"] as? [String: Any], name: "Claude")
        let codex = parse(root["codex"] as? [String: Any], name: "Codex")
        let all = [claude, codex].compactMap { $0 }
        let active = all.max(by: {
            ($0.lastTurn ?? .distantPast) < ($1.lastTurn ?? .distantPast)
        })
        return HudSnapshot(active: active, agents: all, generatedAt: Date())
    }

    private static func parse(_ raw: [String: Any]?, name: String) -> AgentInfo? {
        guard let raw else { return nil }
        let iso = ISO8601DateFormatter()
        iso.formatOptions = [.withInternetDateTime, .withFractionalSeconds]
        let isoNoFrac = ISO8601DateFormatter()
        isoNoFrac.formatOptions = [.withInternetDateTime]
        let lastTurn: Date? = (raw["last_turn_at"] as? String).flatMap {
            iso.date(from: $0) ?? isoNoFrac.date(from: $0)
        }
        let cwd = raw["last_cwd"] as? String ?? ""
        let project = cwd.isEmpty ? "—" : (cwd as NSString).lastPathComponent
        return AgentInfo(
            name: name,
            project: project,
            ctxPct: raw["last_context_pct"] as? Double,
            model: raw["last_model"] as? String,
            session5hPct: raw["session_5h_percent"] as? Double,
            week7dPct: raw["week_7d_percent"] as? Double,
            activeTokens: (raw["active_session_tokens"] as? UInt64)
                ?? UInt64(raw["active_session_tokens"] as? Int ?? 0),
            lastTurn: lastTurn
        )
    }
}

// MARK: - Timeline

struct HudEntry: TimelineEntry {
    let date: Date
    let snapshot: HudSnapshot
}

struct HudProvider: TimelineProvider {
    func placeholder(in context: Context) -> HudEntry {
        HudEntry(date: Date(), snapshot: .empty)
    }
    func getSnapshot(in context: Context, completion: @escaping (HudEntry) -> Void) {
        completion(HudEntry(date: Date(), snapshot: HudSnapshot.load()))
    }
    func getTimeline(in context: Context, completion: @escaping (Timeline<HudEntry>) -> Void) {
        let now = Date()
        let snap = HudSnapshot.load()
        let entry = HudEntry(date: now, snapshot: snap)
        // Refresh every minute — hud.json is regenerated by the menubar app
        // far more often than that; this is the floor.
        let next = now.addingTimeInterval(60)
        completion(Timeline(entries: [entry], policy: .after(next)))
    }
}

// MARK: - Views

private func ctxColor(_ pct: Double?) -> Color {
    guard let pct else { return .secondary }
    switch pct {
    case ..<60: return .green
    case ..<85: return .orange
    default:    return .red
    }
}

private func fmtPct(_ v: Double?) -> String {
    guard let v else { return "—" }
    return String(format: "%.0f%%", v)
}

struct RingView: View {
    let value: Double?
    let label: String
    let tint: Color

    var body: some View {
        ZStack {
            Circle().stroke(Color.secondary.opacity(0.15), lineWidth: 6)
            Circle()
                .trim(from: 0, to: CGFloat(min(max((value ?? 0) / 100.0, 0), 1)))
                .stroke(tint, style: StrokeStyle(lineWidth: 6, lineCap: .round))
                .rotationEffect(.degrees(-90))
            VStack(spacing: 1) {
                Text(fmtPct(value))
                    .font(.system(size: 13, weight: .semibold, design: .rounded))
                    .monospacedDigit()
                Text(label)
                    .font(.system(size: 8, weight: .medium))
                    .foregroundColor(.secondary)
                    .textCase(.uppercase)
            }
        }
    }
}

struct BarRow: View {
    let label: String
    let value: Double?
    let tint: Color
    var body: some View {
        VStack(alignment: .leading, spacing: 2) {
            HStack {
                Text(label).font(.system(size: 9, weight: .medium))
                    .foregroundColor(.secondary).textCase(.uppercase)
                Spacer()
                Text(fmtPct(value))
                    .font(.system(size: 10, weight: .semibold, design: .rounded))
                    .monospacedDigit()
                    .foregroundColor(tint)
            }
            GeometryReader { geo in
                ZStack(alignment: .leading) {
                    Capsule().fill(Color.secondary.opacity(0.12))
                    Capsule().fill(tint)
                        .frame(width: geo.size.width * CGFloat(min(max((value ?? 0) / 100, 0), 1)))
                }
            }
            .frame(height: 4)
        }
    }
}

struct SmallWidgetView: View {
    let entry: HudEntry
    var body: some View {
        let a = entry.snapshot.active
        ZStack {
            ContainerBackground()
            VStack(alignment: .leading, spacing: 8) {
                HStack(spacing: 6) {
                    Circle().fill(ctxColor(a?.ctxPct)).frame(width: 6, height: 6)
                    Text(a?.name ?? "no agent")
                        .font(.system(size: 11, weight: .semibold))
                    Spacer()
                }
                Text(a?.project ?? "—")
                    .font(.system(size: 14, weight: .bold, design: .rounded))
                    .lineLimit(1)
                    .truncationMode(.middle)
                Spacer(minLength: 0)
                RingView(value: a?.ctxPct, label: "context", tint: ctxColor(a?.ctxPct))
                    .frame(width: 56, height: 56)
                    .frame(maxWidth: .infinity, alignment: .center)
            }
            .padding(12)
        }
    }
}

