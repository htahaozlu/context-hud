                        .foregroundColor(.secondary).textCase(.uppercase)
                    ForEach(entry.snapshot.agents.indices, id: \.self) { i in
                        let agent = entry.snapshot.agents[i]
                        HStack {
                            Circle().fill(ctxColor(agent.ctxPct)).frame(width: 5, height: 5)
                            Text(agent.name)
                                .font(.system(size: 11, weight: .medium))
                            Text("·").foregroundColor(.secondary)
                            Text(agent.project)
                                .font(.system(size: 11))
                                .foregroundColor(.secondary)
                                .lineLimit(1).truncationMode(.middle)
                            Spacer()
                            Text(fmtPct(agent.ctxPct))
                                .font(.system(size: 11, weight: .semibold, design: .rounded))
                                .monospacedDigit()
                                .foregroundColor(ctxColor(agent.ctxPct))
                        }
                    }
                }
                Spacer(minLength: 0)
            }
            .padding(16)
        }
    }
}

/// Wrapper that uses `containerBackground` on macOS 14+ and falls back to a
/// custom material fill on earlier systems. WidgetKit will reject widgets on
/// macOS 14+ that don't declare a container background.
struct ContainerBackground: View {
    var body: some View {
        if #available(macOS 14.0, *) {
            Color.clear
        } else {
            LinearGradient(
                colors: [Color(NSColor.windowBackgroundColor),
                         Color(NSColor.controlBackgroundColor)],
                startPoint: .topLeading, endPoint: .bottomTrailing
            )
        }
    }
}

struct WidgetEntryView: View {
    @Environment(\.widgetFamily) var family
    var entry: HudEntry
    var body: some View {
        Group {
            switch family {
            case .systemSmall: SmallWidgetView(entry: entry)
            case .systemMedium: MediumWidgetView(entry: entry)
            case .systemLarge: LargeWidgetView(entry: entry)
            default: MediumWidgetView(entry: entry)
            }
        }
        .modifier(WidgetBackgroundModifier())
    }
}

struct WidgetBackgroundModifier: ViewModifier {
    func body(content: Content) -> some View {
        if #available(macOS 14.0, *) {
            content.containerBackground(for: .widget) {
                LinearGradient(
                    colors: [Color(NSColor.windowBackgroundColor),
                             Color(NSColor.controlBackgroundColor)],
                    startPoint: .topLeading, endPoint: .bottomTrailing
                )
            }
        } else {
            content
        }
    }
}

// MARK: - Widget declaration

@main
struct ContextBarWidget: Widget {
    let kind: String = "ContextBarWidget"
    var body: some WidgetConfiguration {
        StaticConfiguration(kind: kind, provider: HudProvider()) { entry in
            WidgetEntryView(entry: entry)
        }
        .configurationDisplayName("ContextBar")
        .description("Active agent, project, and context usage.")
        .supportedFamilies([.systemSmall, .systemMedium, .systemLarge])
    }
}
